// _MS -> milliseconds
// _S -> seconds
// _M -> minutes
// _H -> hours

/* eslint-disable no-shadow */
export enum Time {
  SECOND_MS = 1000,
  MINUTE_MS = 60000,
  MINUTE_S = 60,
  HOUR_MS = 3600000,
  HOUR_S = 3600,
  HOUR_M = 60,
  DAY_MS = 86400000,
  DAY_S = 86400,
  DAY_M = 1440,
  DAY_H = 24,
}
